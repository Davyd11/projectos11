#include <unistd.h>

int main(int c, char **text)
{
	int x;

	x = 1;
	while (text[x] != '\0')
	{
		c = 0;
		while(text[x][c] != '\0')
		{
			write(1, &(text[x][c]), 1);
			c++;
		}
		write(1, "\n", 1);
		x++;
	}
	return (0);
}
