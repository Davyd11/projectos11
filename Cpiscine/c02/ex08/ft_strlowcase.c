char	*ft_strlowcase(char *str)
{
	int n;

	n = 0;
	while (str[n] != '\0')
	{
		if (str[n] <= 'Z' && str[n] >= 'A')
		{
			str[n] += 32;
		}
		n++;
	}
	return (str);
}
