int	ft_sqrt(int nb)
{
	int p;
	int n;

	p = 0;
	n = 0;
	if (nb <= 0)
		return (0);
	else if (nb > 0)
	{
		while (p != nb)
		{
			p = n * n;
			n++;
		}
		return (n - 1);
	}
	return (0);
}
